    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:
        jButton15.setVisible(true);
        jButton4.setVisible(true);
        int s_d=0,s_m=0,s_y=0,e_d=0,e_m=0,e_y=0;
        String s=(String) i7.getSelectedItem()+"-"+(String) i8.getSelectedItem()+"-"+(String) i9.getSelectedItem();
        String e=(String) i10.getSelectedItem()+"-"+(String) i11.getSelectedItem()+"-"+(String) i12.getSelectedItem();
        if(!s.equals("DD-MM-YYYY")&&!e.equals("DD-MM-YYYY"))
        {
            s_d=Integer.parseInt((String) i7.getSelectedItem());
            s_m=Integer.parseInt((String) i8.getSelectedItem());
            s_y=Integer.parseInt((String) i9.getSelectedItem());
            e_d=Integer.parseInt((String) i10.getSelectedItem());
            e_m=Integer.parseInt((String) i11.getSelectedItem());
            e_y=Integer.parseInt((String) i12.getSelectedItem());
        }
        else if(e.equals("DD-MM-YYYY"))
        {
            e_d=Integer.MAX_VALUE;
            e_m=Integer.MAX_VALUE;
            e_y=Integer.MAX_VALUE;
        }
        else if(s.equals("DD-MM-YYYY"))
        {
            s_d=Integer.MIN_VALUE;
            s_m=Integer.MIN_VALUE;
            s_y=Integer.MIN_VALUE;
        }
        else
        {
            e_d=Integer.MAX_VALUE;
            e_m=Integer.MAX_VALUE;
            e_y=Integer.MAX_VALUE;
            s_d=Integer.MIN_VALUE;
            s_m=Integer.MIN_VALUE;
            s_y=Integer.MIN_VALUE;
        }
        try
        {
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/cybercrime","root","");
            PreparedStatement ps1=con.prepareStatement("select* from case_brief");
            ResultSet rs=ps1.executeQuery();
            while(rs.next())
            {
                //System.out.println("Keri rs undakki");
                String date=rs.getString("date");
                int d=Integer.parseInt(date.substring(0,2));
                int m=Integer.parseInt(date.substring(3,5));
                int y=Integer.parseInt(date.substring(6));
                if(((s_y<=y)||(s_y==y&&s_m<=m)||(s_y==y&&s_m==m&&s_d<=d))&&((e_y>=y)||(e_y==y&&e_m>=m)||(e_y==y&&e_m==m&&e_d>=d)))
                {
                    String com_no=rs.getString("com_no");
                    String name=rs.getString("name");
                    String station=rs.getString("station");
                    String type=rs.getString("type");
                    String status=rs.getString("status");
                    String tbdata[]={com_no,name,station,district,date,type,status};
                    DefaultTableModel tblModel=(DefaultTableModel)table.getModel();

                    tblModel.addRow(tbdata);
                }
            }
        }
        catch(SQLException ex)
        {
            System.out.println("Exception caught"+ex);
        }
    }//GEN-LAST:event_jButton14ActionPerformed

